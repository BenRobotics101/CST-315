#!/bin/bash

# Command 1: Print current date and time
echo "Current date and time:"
date

# Command 2: Print disk usage
echo "Disk usage:"
df -h

# Command 3: List files in the current directory
echo "List of files in the current directory:"
ls -l

# Command 4: Print system information
echo "System information:"
uname -a

# Command 5: Display network interfaces
echo "Network interfaces:"
ifconfig

echo "Script execution complete."
